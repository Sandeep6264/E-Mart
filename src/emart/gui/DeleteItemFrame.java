    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row=jtProductDetails.getSelectedRow();
        if(row==-1){
              JOptionPane.showMessageDialog(null,"not Products selected!");
              return;
        }
        String pid=tm.getValueAt(row, 0).toString();
        try{
            if(ProductDAO.deleteProduct(pid)){
                 JOptionPane.showMessageDialog(null," Products deleted successfully");
                 tm.setRowCount(0);
                   loadProductDetails();
                   return;
            }
            JOptionPane.showMessageDialog(null,"Proudct not deleted");
            }
        catch(SQLException e){
                     JOptionPane.showMessageDialog(null,"DB Error","Error ",JOptionPane.ERROR_MESSAGE);
                     e.printStackTrace();
          }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

