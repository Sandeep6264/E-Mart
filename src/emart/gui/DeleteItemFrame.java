    private void loadProductDetails() {
             try{
          List<ProductPojo> al=ProductDAO.getProductDetails();
          if(al.isEmpty()){
              JOptionPane.showMessageDialog(null,"not products present in Stocks");
              return;
          }
          btnDelete.setEnabled(true);
            Object [] rows;
          rows = new Object[7];
          tm=(DefaultTableModel)jtProductDetails.getModel();
          for(ProductPojo p:al){
              rows[0]=p.getProdutId();
              rows[1]=p.getProductName();
              rows[2]=p.getProductCompany();
              rows[3]=p.getProductPrice();
              rows[4]=p.getOurPrice();
              rows[5]=p.getQuenitity();
              rows[6]=p.getTax()+"%";
              tm.addRow(rows);
          }
             
    }
      catch(SQLException e){
                     JOptionPane.showMessageDialog(null,"DB Error","Error ",JOptionPane.ERROR_MESSAGE);
                     e.printStackTrace();
          }
      catch(Exception e){
          e.printStackTrace();
      }
    }

